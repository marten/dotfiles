-- Now run xmonad with all the defaults we set up.
--
{-main = xmonad defaults-}
main = do
        d <- spawnDzen defaultDzen { width = (Just (Percent 40)), height = Just 16 }
        spawnToDzen "conky -c ~/.dzen_conkyrc" conkyBar
        spawnToDzen "/home/marten/bin/irccloud_tail" notifyBar
        xmonad $ defaults {
          logHook = dynamicLogWithPP $ defaultPP { ppOutput = hPutStrLn d }
        , manageHook  = manageDocks <+> myManageHook
        }
        where
          conkyBar :: DzenConf
          conkyBar = defaultDzen
              { width     = Just (Percent 35)
              , xPosition = Just (Percent 60)
              , height    = Just 16
              , alignment = Just RightAlign
              , fgColor   = Just "#606060"
              }
          notifyBar :: DzenConf
          notifyBar = defaultDzen
              { width     = Just (Percent 20)
              , xPosition = Just (Percent 40)
              , height    = Just 16
              , alignment = Just RightAlign
              , fgColor   = Just "#aaaaaa"
              }
 
